    @Override
    public void propertyChange(PropertyChangeEvent evt) {
        //on hit
        if (evt.getPropertyName().equals("hit")) {
            simulation.stop();
            if (evt.getNewValue().toString().equals("Der blaue Verfolger") || evt.getNewValue().toString().equals("Der rote Verfolger")) {
                textfieldHint.setText("" + evt.getNewValue().toString() + " hat das Ziel eingeholt!");
            } else {
                textfieldHint.setText("Das Ziel hat den Rand des Spielfelds erreicht!");
            }
            buttonOnOff.setEnabled(false);
            buttonReset.setEnabled(true);
            simulation.paintRunnerDirection = false;
            simulation.mouseControlEnabled = false;
            animation.repaint();
            if (buttonOnOff.getText()
                    .equals("STOP")) {
                buttonOnOff.setText("START");
            }
        }

        Vector v;
        if (evt.getPropertyName().equals("nextRunner")) {
            v = (Vector) evt.getNewValue();
            numberfieldPosRunnerY.setText("" + String.format("%6.2f", v.y));
            numberfieldPosRunnerX.setText("" + String.format("%6.2f", v.x));
        }

        if (evt.getPropertyName().equals("nextChaserBlue")) {
            v = (Vector) evt.getNewValue();
            numberfieldPosChaserBlueY.setText("" + String.format("%6.2f", v.y));
            numberfieldPosChaserBlueX.setText("" + String.format("%6.2f", v.x));
        }

        if (evt.getPropertyName().equals("nextChaserRed")) {
            v = (Vector) evt.getNewValue();
            numberfieldPosChaserRedY.setText("" + String.format("%6.2f", v.y));
            numberfieldPosChaserRedX.setText("" + String.format("%6.2f", v.x));
        }
        
        if (evt.getPropertyName().equals("velocityChangeRunner")) {
            numberfieldVelRunner.setText(String.format("%6.2f", (double)evt.getNewValue() * 100 / simulation.getTimeScale()));
        }
        
        if (evt.getPropertyName().equals("velocityChangeChaserBlue")) {
            numberfieldVelChaserBlue.setText(String.format("%6.2f", (double)evt.getNewValue() * 100 / simulation.getTimeScale()));
        }
        
        if (evt.getPropertyName().equals("velocityChangeChaserRed")) {
            numberfieldVelChaserRed.setText(String.format("%6.2f", (double)evt.getNewValue() * 100 / simulation.getTimeScale()));
        }
        
        
    }

